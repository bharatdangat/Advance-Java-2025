
public class Marcks
{
  double theorymarcks;
  double practicalmarcks;

  public Marcks(double theorymarcks, double practicalmarcks)
  {
	super();
	this.theorymarcks = theorymarcks;
	this.practicalmarcks = practicalmarcks;
}

public double getTheorymarcks() {
	return theorymarcks;
}

public void setTheorymarcks(double theorymarcks) {
	this.theorymarcks = theorymarcks;
}

public double getPracticalmarcks() {
	return practicalmarcks;
}

public void setPracticalmarcks(double practicalmarcks) {
	this.practicalmarcks = practicalmarcks;
}
  
}
